<h2>
Effects of Global Warming
</h2>
<br/>
Global warming has been detected in a number of natural systems. Some of these changes are described in the section on observed temperature changes, e.g., sea level rise and widespread decreases in snow and ice extent.[120] Most of the increase in global average temperature since the mid-20th century is, with high probability,[D] attributable to human-induced changes in greenhouse gas concentrations.
Changes in regional climate are expected to include greater warming over land, with most warming at high northern latitudes, and least warming over the Southern Ocean and parts of the North Atlantic Ocean.[122] Snow cover area and sea ice extent are expected to decrease, with the Arctic expected to be largely ice-free in September by 2037.
It is calculated that, with high statistical confidence, certain weather events, such as the heat waves in Texas and the 2003 European heat wave, would not have occurred without global warming. Extremely hot outliers, defined as three standard deviations from climatology records, now cover about 10% of the land surface and, under present trends, would be the norm by 2050. These temperatures are expected to exacerbate the hydrological cycle, with more intense droughts and floods.[126] The effect on hurricane activity is less certain.
In terrestrial ecosystems, the earlier timing of spring events, and poleward and upward shifts in plant and animal ranges, have been linked with high confidence to recent warming.[120] Future climate change is expected to particularly affect certain ecosystems, including tundra, mangroves, and coral reefs.[122] It is expected that most ecosystems will be affected by higher atmospheric CO2 levels, combined with higher global temperatures.[128] Overall, it is expected that climate change will result in the extinction of many species and reduced diversity of ecosystems.  
